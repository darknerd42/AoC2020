    private fun solvePart1(program: Program): Long? {
        computer.loadProgram(program)

        return computer.run()?.result?.toLong()
    }

    private fun solvePart2(instructions: Program): Long? {
        instructions.forEachIndexed { index, instruction ->
            if (instruction.operation == "nop" || instruction.operation == "jmp") {
                val modified = instructions.toMutableList()
                modified[index] = Computer.Instruction(if (instruction.operation == "nop") "jmp" else "nop", instruction.argument)

                computer.loadProgram(modified)
                val result = computer.run() ?: return null

                if (result.terminatedNormally ) return result.result.toLong()
            }
        }
        return null
    }

